package net.mcft.copy.aho.config;

public enum EnumControl {
	
	/** Config settings are used as default
	 *  in the world creation screen. */
	DEFAULT,
	
	/** Config settings are locked in and can't
	 *  be changed in the world creation screen. */
	LOCK,
	
	/** Config settings are used automatically
	 *  and world creation screen is not modified. */
	HIDDEN
	
}
